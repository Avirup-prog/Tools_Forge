/**
 * tool-page-api.js
 * Activates the upload drop zone on API tool pages.
 * Include AFTER api-client.js.
 *
 * Each tool page defines window.TOOL_CONFIG = { endpoint, fields, ... }
 * This script reads that config and wires everything up.
 *
 * Example tool page usage:
 *   <script>
 *     window.TOOL_CONFIG = {
 *       endpoint: '/api/pdf/to-word',
 *       multiFile: false,   // true for merge-pdf, collage, image-to-pdf
 *       extraFields: {},    // static extra form fields
 *       // dynamic fields come from form inputs read at submit time
 *       fieldSelectors: {
 *         quality: '#qualityInput',
 *       },
 *     };
 *   </script>
 *   <script src="../assets/js/api-client.js"></script>
 *   <script src="../assets/js/tool-page-api.js"></script>
 */

(function () {
  const cfg = window.TOOL_CONFIG || {};
  const endpoint = cfg.endpoint;
  if (!endpoint) return; // not an API page

  // ── DOM refs (generated by the Jinja2 template) ──────────────────────────
  const dropZone     = document.getElementById("dropZone");
  const fileInput    = dropZone && dropZone.querySelector("input[type=file]");
  const progressWrap = document.getElementById("uploadProgress");
  const progressBar  = document.getElementById("progressBar");
  const progressLbl  = document.getElementById("progressLabel");
  const progressPct  = document.getElementById("progressPct");
  const toast        = document.getElementById("toast");
  const cssBanner    = document.querySelector(".coming-soon-banner");

  if (!dropZone || !fileInput) return;

  // ── Remove "coming soon" banner now that backend is live ─────────────────
  if (cssBanner) cssBanner.remove();

  // ── Toast helper ──────────────────────────────────────────────────────────
  function showToast(msg, type = "info") {
    if (!toast) return;
    toast.textContent = msg;
    toast.className = `toast toast-${type} show`;
    clearTimeout(toast._t);
    toast._t = setTimeout(() => toast.classList.remove("show"), 4000);
  }

  // ── Progress helpers ──────────────────────────────────────────────────────
  function setProgress(pct) {
    if (!progressWrap) return;
    progressWrap.style.display = "block";
    if (pct === -1) {
      // Cold-start wait signal
      progressLbl.textContent = "Still processing… (server may be warming up)";
      return;
    }
    progressBar.style.width = pct + "%";
    progressPct.textContent = pct + "%";
    progressLbl.textContent = pct < 100 ? "Uploading…" : "Processing on server…";
  }

  function resetProgress() {
    if (!progressWrap) return;
    progressWrap.style.display = "none";
    progressBar.style.width = "0%";
    progressPct.textContent = "0%";
    progressLbl.textContent = "Uploading…";
  }

  // ── Build dynamic fields from the page's form inputs ─────────────────────
  function buildFields() {
    const fields = { ...(cfg.extraFields || {}) };
    const selectors = cfg.fieldSelectors || {};
    for (const [key, sel] of Object.entries(selectors)) {
      const el = document.querySelector(sel);
      if (el) {
        fields[key] = el.type === "checkbox" ? el.checked : el.value;
      }
    }
    return fields;
  }

  // ── File handling ─────────────────────────────────────────────────────────
  function handleFiles(fileList) {
    const files = Array.from(fileList);
    if (!files.length) return;

    resetProgress();
    setProgress(0);
    showToast("Uploading file…", "info");

    const uploadArgs = {
      endpoint,
      fields: buildFields(),
      onProgress: setProgress,
      onSuccess: (blob, filename) => {
        resetProgress();
        showToast(`✅ Done! Downloading ${filename}`, "success");
        ToolForgeAPI.download(blob, filename);
      },
      onError: (msg) => {
        resetProgress();
        showToast(`❌ ${msg}`, "error");
      },
    };

    if (cfg.multiFile) {
      uploadArgs.files = files;
    } else {
      uploadArgs.file = files[0];

      // fill-sign: second file is "signature"
      if (endpoint.includes("fill-sign") && files[1]) {
        const form = new FormData();
        form.append("pdf_file", files[0]);
        form.append("signature", files[1]);
        const fields = buildFields();
        for (const [k, v] of Object.entries(fields)) form.append(k, v);
        _sendFillSign(form, uploadArgs.onSuccess, uploadArgs.onError);
        return;
      }
    }

    ToolForgeAPI.upload(uploadArgs);
  }

  async function _sendFillSign(form, onSuccess, onError) {
    setProgress(50);
    try {
      const resp = await fetch(ToolForgeAPI.BASE_URL + endpoint, {
        method: "POST",
        body: form,
      });
      if (!resp.ok) {
        const d = await resp.json().catch(() => ({}));
        throw new Error(d.detail || `Error ${resp.status}`);
      }
      const blob = await resp.blob();
      const cd   = resp.headers.get("Content-Disposition") || "";
      const m    = cd.match(/filename="([^"]+)"/);
      onSuccess(blob, m ? m[1] : "result.pdf");
    } catch (e) {
      onError(e.message);
    } finally {
      resetProgress();
    }
  }

  // ── Wire up file input ────────────────────────────────────────────────────
  fileInput.addEventListener("change", (e) => handleFiles(e.target.files));

  // ── Drag & drop ───────────────────────────────────────────────────────────
  dropZone.addEventListener("dragover", (e) => {
    e.preventDefault();
    dropZone.classList.add("drag-over");
  });
  dropZone.addEventListener("dragleave", () => dropZone.classList.remove("drag-over"));
  dropZone.addEventListener("drop", (e) => {
    e.preventDefault();
    dropZone.classList.remove("drag-over");
    handleFiles(e.dataTransfer.files);
  });

  // ── URL shortener special case ─────────────────────────────────────────────
  if (endpoint.includes("shorten-url")) {
    const urlInput  = document.getElementById("urlInput");
    const shortenBtn = document.getElementById("shortenBtn");
    const resultBox  = document.getElementById("shortenResult");

    if (shortenBtn && urlInput) {
      shortenBtn.addEventListener("click", async () => {
        const url = urlInput.value.trim();
        if (!url) { showToast("Please enter a URL", "error"); return; }
        shortenBtn.disabled = true;
        showToast("Shortening…", "info");
        try {
          const data = await ToolForgeAPI.post(endpoint, { url });
          if (resultBox) {
            resultBox.style.display = "block";
            resultBox.querySelector(".short-url").textContent = data.short;
          }
          showToast(`✅ Shortened! ${data.short}`, "success");
        } catch (e) {
          showToast(`❌ ${e.message}`, "error");
        } finally {
          shortenBtn.disabled = false;
        }
      });
    }
  }
})();
